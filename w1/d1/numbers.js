// What will expression evaluate too?
console.log(2 + 3 * 12);// 38
console.log(10 - 8 / 2);// 6
console.log(20 / 5);// 4
console.log(1.11 * 2);// 2.22
console.log((4 + 5) / 3);// 3
console.log((2 - 2) * 3);// 0
console.log(Infinity / 1);// Infinity
console.log(0.1 + 0.2);// 0.30000000000000004

// Modulo
// * return a remainder
console.log(13 % 5);// 3
// * we divide 5 into 13 twice, there will be a remainder of 3

console.log(3 % 7);// 3
// *  3 cant divide by 7, we return 3

